create database WEATHER_FORECAST
use WEATHER_FORECAST 

CREATE TABLE ACCOUNT (
		_username char(20),
		_password char(20),
		_type int
		primary key (_username)
)

create table CITY (
		_ID CHAR(5),
		_NAME VARCHAR(50)
		PRIMARY KEY (_ID)
)

CREATE TABLE CITY_INFO(
		CITY_ID CHAR(5),
		WEATHER_DATE DATE,
		TEMPERATURE FLOAT,
		WIND FLOAT,
		CLOUDS FLOAT,
		PRESSURE FLOAT
		CONSTRAINT PK_WT PRIMARY KEY (CITY_ID,WEATHER_DATE)
)

ALTER TABLE CITY_INFO
ADD CONSTRAINT FK_INFO_CITY
FOREIGN KEY (CITY_ID)
REFERENCES CITY(_ID)

